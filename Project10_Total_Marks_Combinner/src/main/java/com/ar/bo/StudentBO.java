package com.ar.bo;

public class StudentBO {
	
	//Bean Properties
	private String studentName;
	private Float studentClass;
	private String exam;
	private Float telugu;
	private Float english;
	private Float mathematics;
	private Float science;
	private Float social;
	private Float totalMarks;
	//Getters and Setters
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public Float getStudentClass() {
		return studentClass;
	}
	public void setStudentClass(Float studentClass) {
		this.studentClass = studentClass;
	}
	public String getExam() {
		return exam;
	}
	public void setExam(String exam) {
		this.exam = exam;
	}
	public Float getTelugu() {
		return telugu;
	}
	public void setTelugu(Float telugu) {
		this.telugu = telugu;
	}
	public Float getEnglish() {
		return english;
	}
	public void setEnglish(Float english) {
		this.english = english;
	}
	public Float getMathematics() {
		return mathematics;
	}
	public void setMathematics(Float mathematics) {
		this.mathematics = mathematics;
	}
	public Float getScience() {
		return science;
	}
	public void setScience(Float science) {
		this.science = science;
	}
	public Float getSocial() {
		return social;
	}
	public void setSocial(Float social) {
		this.social = social;
	}
	public Float getTotalMarks() {
		return totalMarks;
	}
	public void setTotalMarks(Float totalMarks) {
		this.totalMarks = totalMarks;
	}
	
	
	
	
	

}
